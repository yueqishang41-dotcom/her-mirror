function safetyCheck(text) {
  if (!text || text.trim() === '') return 'green';

  // 特殊处理：试探性问题
  const isExploratory = text.includes('听说') || text.includes('什么是') ||
                         text.includes('有人') || text.includes('帮朋友问') || text.includes('朋友问');

  if (isExploratory) {
    for (const k of crisisKeywords.diagnosisRequest) {
      if (text.includes(k)) return 'yellow';
    }
    if (text.includes('自残') && !text.includes('我自残') && !text.includes('我割')) {
      return 'yellow';
    }
  }

  // 红灯
  for (const k of crisisKeywords.selfHarm) if (text.includes(k)) return 'red';
  for (const k of crisisKeywords.violence) if (text.includes(k)) return 'red';
  for (const k of crisisKeywords.despair) if (text.includes(k)) return 'red';

  // 黄灯
  for (const k of crisisKeywords.emotionalLow) if (text.includes(k)) return 'yellow';
  for (const k of crisisKeywords.diagnosisRequest) if (text.includes(k)) return 'yellow';
  for (const k of crisisKeywords.dependency) if (text.includes(k)) return 'yellow';

  return 'green';
}

